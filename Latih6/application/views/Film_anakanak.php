<p><ol> <h2>
<li> <a href="https://id.wikipedia.org/wiki/Minions_(film)" target="_blank">  Minions </a> <br>
<center><img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Minions.jpg"> </center> <br>
	
	Sutradara: Kyle Balda, Pierre Coffin <br>
	Pemain utama: Pierre Coffin, Sandra Bullock, Jon Hamm <br>
	Produksi: Illumination Entertainment <br><br>
	
	Tiga minions bernama Stuart, Kevin, dan Bob mencari bos jahat baru dan mereka direkrut oleh Scarlet<br>Overkill, penjahat super yang bersama suami penemunya, Herb, memiliki rencana untuk menguasai dunia. <br><br>

	Fakta unik: <br>
	– Sutradara Pierre Coffin mengisi suara bagi 899 minions dalam film ini. <br>
	– Minions terdengar mengucapkan kata ‘thank you’ dalam beberapa bahasa, termasuk ‘terima kasih’ dalam Bahasa Indonesia dan ‘gracias’ dalam bahasa Spanyol.	<br>
	– Film pertama di mana Sandra Bullock memerankan karakter jahat, meski hanya sebagai pengisi suara. <br><br><br> 

<li> <a href="https://en.wikipedia.org/wiki/Home_(2015_film)" target="_blank"> Home </a> <br>
<center><img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Home.jpg"></center> <br>
	
	Sutradara: Tim Johnson<br>
	Pemain utama: Jim Parsons, Rihanna, Steve Martin <br> 
	Produksi: DreamWorks Animation <br><br>

	Oh, alien yang lari dari bangsanya di planet lain, mendarat di bumi dan bersahabat dengan gadis petualang bernama Tip. Dalam petualangan Oh dan Tip, mereka berdua mempelajari banyak hal yang berarti.<br><br>

	Fakta unik:<br>
	– Penyanyi top Rihanna ditunjuk mengisi suara karakter Tip. Di samping itu, ia juga menyumbangkan beberapa lagu untuk soundtrack film ini.<br>
	– Karakter Oh awalnya hendak dinamai J.Lo, namun diganti karena alasan tertentu. J.Lo adalah nama panggilan Jennifer Lopez, yang secara kebetulan juga mengisi suara dalam film ini.<br>
	– Karakter Rihanna dalam film ini, Tip, berasal dari Barbados, sama seperti dirinya.  <br><br><br>

<li><a href="https://en.wikipedia.org/wiki/Inside_Out_(2015_film)" target="_blank"> Inside Out</a><br>
<center> <img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Inside-Out1.jpg"> </center> <br>
	Sutradara: Pete Docter, Ronaldo Del Carmen <br>
	Pemain utama: Amy Poehler, Bill Hader, Lewis Black <br>
	Produksi: Pixar Animation Studios, Walt Disney Pictures<br><br>

	Setelah Riley muda beranjak dari kehidupannya di Midwest dan pergi ke San Francisco, emosinya -Joy, Fear, Anger, Disgust, dan Sadness- mulai berkonflik dan memberi berbagai saran pada Riley dalam kehidupan sehari-harinya.<br><br>

	Fakta unik:<br>
	– Penulis film ini membuat 27 emosi yang berbeda, namun hanya memilih lima untuk membuatnya lebih sederhana.<br>
	– Film ini hanya digarap oleh 45 animator, jumlah terkecil dari seluru film garapan Pixar.<br>
	– Kepribadian yang berbeda dari berbagai emosi ini terinspirasi dari para kurcaci dalam film Snow White and the Seven Dwarfs (1937).<br><br><br>

<li> <a href="https://id.wikipedia.org/wiki/The_SpongeBob_Movie:_Sponge_Out_of_Water" target="_blank">  The SpongeBob Movie: Sponge Out of Water</a><br>
<center> <img src="http://www.kembangpete.com/wp-content/uploads/2015/06/SpongeBob.jpg"> </center> <br>
	
	Sutradara: Paul Tibbitt<br> 
	Pemain utama: Tom Kenny, Antonio Banderas, Bill Fagerbakke <br>
	Produksi: Paramount Animation, Nickelodeon Movies, United Plankton Pictures <br><br>

	Ketika seorang bajak laut jahat di atas laut mencuri formula rahasia milik Krabby Patty, seluruh penghuni Bikini Bottom percaya bahwa SpongeBob dan Plankton, musuh bebuyutannya, adalah pencurinya. Mau tak mau keduanya harus bekerjasama untuk mendapatkannya kembali. <br><br>

	Fakta unik:<br>
	– Ini adalah film animasi studio Hollywood pertama yang menggunakan animasi gambaran tangan sejak peluncuran Winnie the Pooh (2011).<br>
	– Film ini didedikasikan untuk mengenang Ernest Borgnine (1917-2012), yang mengisi suara karakter Mermaid Man dalam SpongeBob SquarePants (1999). <br>
	– Staf acara SpongeBob SquarePants dari musim satu sampai tiga kembali untuk mengerjakan film ini, terutama si pencipta SpongeBob, Stephen Hillenburg, yang kini menggarap episode SpongeBob secara penuh.<br><br><br>

<li> <a href="https://id.wikipedia.org/wiki/Shaun_the_Sheep_Movie" target="_blank">Shaun the Sheep Movie</a><br>
<center> <img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Shaun-the-Sheep-Movie.jpg"> </center> <br>

	Sutradara: Mark Burton, Richard Starzak<br>
	Pemain utama: Justin Fletcher, John Sparkes, Omid Djalili<br>
	Produksi: StudioCanal, Aardman Animations, Anton Capital Entertainment (ACE)<br><br>

	Suatu ketika Shaun ingin beristirahat dan berjalan-jalan sambil bersenang-senang, namun karena serangkaian peristiwa unik Shaun dan kawan-kawannya malah masuk ke Big City. Mereka harus mencari cara untuk kembali ke rumah padang rumputnya.<br><br>

	Fakta unik:<br>
	– 20 animator mengerjakan film ini. Setiap orang memproduksi dua detik dari panjang film ini setiap harinya.<br>
	– Film ini benar-benar tidak memiliki dialog apapun selain suara-suara hewan.<br>
	– Fotografer yang mengambil foto burung di kolam adalah penampilan cameo dari Nick Park, pencipta Wallace and Gromit, yang menjabat sebagai eksekutif produser dalam film ini.<br><br><br>

<li> <a href="https://id.wikipedia.org/wiki/The_Little_Prince_(film_2015)" target="_blank"> The Little Prince (2015) </a><br>
<center> <img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Little-Prince.jpg"> </center> <br>

	Sutradara: Mark Osborne<br>
	Pemain utama: Rachel McAdams, James Franco, Paul Rudd<br>
	Produksi: Onyx Films, Orange Studio, On Entertainment<br><br>

	Seorang gadis kecil yang tak kenal takut pada suatu hari bertemu dengan The Aviator, tetangganya, seorang pria eksentrik. Gadis itu kemudian akan mendengar kisah tentang seorang pilot yang jatuh di padang pasir dan bertemu dengan seorang bocah dari planet yang jauh.<br><br>

	Fakta unik:<br>
	– Marion Cotillard mengisi suara karakter The Rose dalam versi bahasa Inggris dan Perancis.<br>

	– Barisan pengisi suara film ini memiliki tiga pemenang Oscar: Benicio Del Toro, Marion Cotillard, dan Jeff Bridges serta tiga nominator Oscar: Albert Brooks, Paul Giamatti, dan James Franco.<br>

	– Putri kecil adalah kisah favorit dari aktor James Dean yang merupakan salah satu idola James Franco.<br><br><br>

<li> <a href="https://en.wikipedia.org/wiki/Strange_Magic_(film)" target="_blank"> Strange Magic </a><br>
<center> <img src="http://www.kembangpete.com/wp-content/uploads/2015/06/Strange-Magic.jpg"> </center> <br>

	Sutradara: Gary Rydstrom<br>
	Pemain utama: Evan Rachel Wood, Elijah Kelley, Kristin Chenoweth <br>
	Produksi: Industrial Light and Magic (ILM), Lucasfilm Animation Singapore, Lucasfilm Animation, Lucasfilm, Touchstone Pictures<br><br>

	Goblin, elf, peri, dan imp, dan kesialan mereka yang lucu dipicu oleh perebutan ramuan kuat. Bog King, pemimpin Dark Forest, membenci gagasan tentang cinta dan memerintahkan penghancuran semua bunga mawar, yang merupakan unsur penting dari ramuan cinta.<br><br>

	Fakta unik:<br>
	– Desain Bog King didasarkan pada belalang sembah dan kecoa.<br>
	– George Lucas, pendiri Lucasfilm, menghabiskan waktu selama lima tahun bersama supervisor musik Steven Gizicki untuk memilih lagu yang tepat untuk film ini. <br>
	– Dua aktor utama film ini sebelumnya pernah bekerjasama dengan Lucasfilm, yakni Elijah Kelley (pengisi suara Sunny) dalam Red Tails dan Alfred Molina (pengisi suara Fairy King) dalam Raiders of the Lost Ark.<br><br>
