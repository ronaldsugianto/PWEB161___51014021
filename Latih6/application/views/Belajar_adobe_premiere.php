	Berikut ini adalah beberapa cara mudah untuk mengedit video dengan Adobe Premiere :

<h3> CAPTURE </h3>

Proses capture gambar/ video merupakan proses pemindahan gambar dari memori penyimpanan (MiniDV atau MicroSD) ke dalam hardisk komputer/ laptop Anda. Adapun cara untuk meng-capture adalah sebagai berikut ini :<br>
<ol>
   <li> Aktifkan kamera/ handycam yang Anda miliki
   <li> Aktifkan program aplikasi Adobe Premiere pada komputer/ laptop Anda
   <li> Pada kotak Digital Video Device, klik menu Edit and Record Video. Kemudia klik OK
   <li> Pilih New Project untuk memulai project baru
   <li> Pilih menu DV PAL atau DV NTSC, kemudian sesuaikan dengan jenis kamera yang Anda gunakan. Untuk kamera yang biasa digunakan di Indonesia, pilih PAL. Setelah itu isi nama project yang Anda buat pada kotak Name. Diikuti dengan memilih tombol OK
   <li> Maka akan muncul tampilan antarmuka Adobe Premiere
   <li> Pilih menu File >> Campture
   <li> Pilih direktori yang Anda gunakan untuk menempatkan hasil capture video. Setelah itu klik Setting
   <li> Klik Browse  untuk mencari video kemudian Klik OK.
   <li> Ikuti langkah yang sama untuk capture Audio
   <li> Gunakan perintah dan tombol yang sama seperti diatas untuk dapat membuka video yang ada di dalam MiniDV / Micro SD Anda
   <li> Setelah semua selesai, pilih tombol Play dan Record untuk memulai proses capture
   <li> Pilih tombol Stop, jika akan menghentikan proses capture.
   <li> Ketik nama video klip Anda dalam kotak Clip name dengan ektensi .avi
   <li> Klik OK untuk menyimpan klip video dalam panel project.
</ol>

<h3> EDITING DALAM SOURCE MONITOR </h3>

Proses editing dilakukan untuk memotong beberapa klip video  yang mungkin ingin Anda buang atau Anda simpan, caranya :<br>
<ol>
    <li> Drag and Drop klip video yang sebelumnya telah dismpan ke dalam Source Monitor
    <li> Geser klip videp untuk menentukan potongan awal, setelah itu klik In Point
    <li> Geser klip video untuk memnentukan potongan akhir, setelah itu klik Out Point
    <li> Klik dan geser klip video ke dalam timeline
    <li> Ulangi cara 1 hingga 4 pada bagian lainnya.
</ol>

<h3> EDITING DALAM TIMELINE </h3>

Jika terdapat bagian yang masih ingin Anda hilangkan, lakukan proses dengan menggunakan banyak alat untuk mengedit klip di dalam timeline seperti dibawah ini : <br>
<ol>
    <li>Razor Tool : Memotong klip
    <li>Move Tool : Memilih Klip
    <li>Rate Stretch Tool : Mengatur kecepatan putar klip
</ol>
<h3>PENAMBAHAN  EFEK </h3>

klip-klip yang ada di dalam timeline dapat diberikan efek tambahan untuk menjadikannya lebih menarik. pilih efek dalam tab Effects, lalu Klik dan geser klip video yang Anda inginkan. Aturlah efek pada tab Effect Controls. Dan lihat hasil akhirnya pada layar.

<h3> PENAMBAHAN TRANSISI </h3>

Proses penambahan transisi dilakukan agar terjadi  perpindahan antar dua  atau beberapa klip menjadi lebih halus. Pilih tipe transisi yang  terdapat pada  folder Video Transitions. Kemudian geserlah klip diantara pertemua dua klip. Atur transisi yang telah ditambahkan  dalam tab Effect Controls.

<h3>MENGGABUNGKAN KLIP VIDEO </h3>

Terapat cara yang mudah untuk menggabungkan klip video, yaitu simpan  video 1 pada track Video 1, kemudia simpan video yang ingin Anda gabungkan pada track video 2, berada di atas video 1.

Untuk menggabungkan kedua klip tersebut , klik klip video 1,  pilih tab Effect Controls, aturlah  Opacity sesuai selera Anda. Untuk dapat menambahkan klip video dari  source yang  lain ke dalam panel Project, pilihkan menu  File >> Import.

<h3>MENYIMPAN PROYEK </h3>

Simpanlah proyek  yang telah Anda buat dengan memilih menu File >> Save, atau tekan tombol Ctrl+S secara bersamaan pada keyboard.

<h3>RENDERING  VIDEO </h3>
<ol>
    <li>Pilih menu File >> Export  >> Movie (Untuk Premiere Pro 2).
    <li>Pilih menu File >> Export >> Media (Untuk Premiere Pro CS3dan CS4 dengan Output Mpeg)
    <li>Pilih menu File >> Export >>Adobe Media Encoder (Untuk Premiere Pro 2)
    <li>Pilih menu File >> Export >> Media ( Untuk Premiere Pro CS3 dan CS4). Waktu yang dibutuhkan untuk melakukan proses rende tergantung pada durasi di dalam timeline dan juga dipengaruhi oleh spesifikasi komputer yang digunakan.
</ol>

