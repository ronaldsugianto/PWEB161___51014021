<h1><center> 4 Film Yang Penuh Dengan Motivasi Dan Inspirasi Terbaik </center> </h1> <hr> <br>

<p>
<h2>
	<ol>
		<li> Freaks (1932) <br>
			<img src="https://i2.wp.com/www.beranda.co.id/wp-content/uploads/2015/09/Film-Inspirasi-Limitless.jpg"><br>	
			Film ini diperankan oleh Bradle Cooper yang berperan sebagai Eddie Morra. Ia adalah seorang penulis yang sering sekali menunda-nunda pekerjanya. Film ini akan memberikan pencerahan bagi penontonya agar bisa melakukan apapun yang dapat ia lakukan saat ini juga. Bukan hanya itu saja, film inspirasi terbaik ini juga mengajarkan kepada anda bagaimana cara menghasilkan sesuatu di dalam hidup anda. Pelajaran yang dapat diambil dari film Limitless ini adalah “Jangan pernah meremehkan diri anda sendiri”.
			 <br><br>


		<li>  In Pursuit Of Happiness <br>
			<img src="https://i1.wp.com/www.beranda.co.id/wp-content/uploads/2015/09/Film-Inspirasi-In-Pursuit-Of-Happiness.png"><br>	
			Hampir seluruh penonton yang menonton film ini akan menangis. Film ini diperankan oleh Will Smith sebagai seorang salesman Chris Gerdner yang menjadi seorang gelandangan. Film yang menginspirasi ini menunjukkan berbagai macam alasan mengapa anda tidak boleh menyerah serta jangan pernah untuk membiarkan lingkungan yang ada di sekitar menghancurkan semua mimpin-mimpi anda saat ini. <br>
			Pelajaran yang dapat anda ambil dari film ini adalah “Jangan pernah menyerah”. <br>
			“Jangan pernah biarkan orang lain mengatakan kepada anda bahwa anda tidak dapat melakukan apapun. Jika anda punya mimpi, maka Lindungilah mimpi tersebut sebaik-baiknya. Jika anda ingin mencapainya, berusahalah dengan sekuat tenaga”. – In Pursuit Of Happiness
			 <br><br>

		<li>  The words <br>
			<img src="https://i1.wp.com/www.beranda.co.id/wp-content/uploads/2015/09/Film-Inspirasi-The-words.jpg"><br>	
			Film ini merupakan film inspirasi terbaik yang menyoroti penulis yang sedang bekerja dengan sangat keras. Film ini menunjukkan betapa kesuksesan itu sulit untuk dicapai, akan tetapi anda tidak boleh pernah menyerah ataupun meniru orang lain. Film ini mengajarkan kepada anda bagaimana cara menguasai pekerjaan anda sendiri, serta bisa menuntaskan pekerjaan anda dengan segala kemampuan yang anda miliki.
			<br>
			Pelajaran yang dapat anda ambil dari film ini adalah “Kuasa pekerjaan Anda”.
			 <br><br>

		<li> The Wolf Of Wall Street <br>
			<img src="https://i2.wp.com/www.beranda.co.id/wp-content/uploads/2015/09/Film-Inspirasi-The-Wolf-Of-Wall-Street.jpg"><br>	
			Film inspirasi terbaik The Wolf Of Wall Street diangkat dari sebuah kisah nyata dari Jordan Belfort. Film ini mendemonstrasikan bermacam-macam hal, mulai dari yang bisa dibeli dengan uang, sampai yang tidak bisa di beli dengan uang. Film ini mengajarkan kepada anda untuk selalu memperbaiki diri untuk mencapai sebuah kesuksesan yang anda tuju.
			<br>
			Pelajaran yang dapat anda ambil dari film ini adalah “Evaluasi terhadap pekerjaan serta menentukan tujuan hidup yang jauh lebih baik”.
			<br>
			“Tidak anda penghargaan di dalam kemiskinan. Saya pernah menjadi orang kaya dan orang miskin. Dan saya tetap akan memilih menjadi orang yang kaya”. 
			 <br><br>
